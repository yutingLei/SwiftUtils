    /// Instanced color with R/G/B. default A = 1
    ///
    /// - Parameter rgb: Red/Blue/Green, the value of R/G/B can be between 0 with 255, also 0~1
    /// - Returns: Instacne of color
    public class func rgb(_ rgb: CGFloat...) -> UIColor {
        assert(rgb.count == 3, "Usage: it must contain 3 values \t\n UIColor.rgb(255, 255, 255)")
        let colors = rgb.map({ $0 > 1 ? ($0 / 255.0) : $0})
        return UIColor(red: colors[0], green: colors[1], blue: colors[2], alpha: 1)
    }

    /// Instanced color with R/G/B/A.
    ///
    /// - Parameter rgba: Red/Blue/Green/Alpha, the value of R/G/B/A can be between 0 with 255, also 0~1
    /// - Returns: Instacne of color
    public class func rgba(_ rgba: CGFloat...) -> UIColor {
        assert(rgba.count == 4, "Usage: it must contain 4 values \t\n UIColor.rgba(255, 255, 255, 1.0)")
        let colors = rgba.map({ $0 > 1 ? ($0 / 255.0) : $0})
        return UIColor(red: colors[0], green: colors[1], blue: colors[2], alpha: colors[3])
    }


    /// Using hex string to init color
    ///
    /// - Parameter hex: The hex string.
    ///                   Formats:  [0x | OX][ff00ff00 | ff00ff | f0f0 | fff]
    ///                                      [RRGGBBAA | RRGGBB | RGBA | RGB]
    public convenience init(hex: String) {

        /// Reject strings
        let hexString = hex.replacingOccurrences(of: "0x", with: "")
        let valuesStr = hexString.replacingOccurrences(of: "0X", with: "")

        /// Process values
        var r = 0, g = 0, b = 0, a = 1
        switch valuesStr.count {
        case 8, 6:
            r = Int(valuesStr[0...1], radix: 16) ?? 0
            g = Int(valuesStr[2...3], radix: 16) ?? 0
            b = Int(valuesStr[4...5], radix: 16) ?? 0
            if valuesStr.count == 8 {
                a = Int(valuesStr[6...7], radix: 16) ?? 0
            }
        case 4, 3:
            r = Int(valuesStr[0...0], radix: 16) ?? 0
            g = Int(valuesStr[1...1], radix: 16) ?? 0
            b = Int(valuesStr[2...2], radix: 16) ?? 0
            if valuesStr.count == 4 {
                a = Int(valuesStr[3...3], radix: 16) ?? 0
            }
        default:
            break
        }
        print("Color's RGBA is: \(r)\t\(g)\t\(b)\t\(a)")
        self.init(red: CGFloat(r), green: CGFloat(g), blue: CGFloat(b), alpha: CGFloat(a))
    }
